package loopDemo;

public class Main {

	public static void main(String[] args) {
		
		//for döngüsü
		
		for(int i=1; i<=10; i++) {
		System.out.println(i);
		}

		//while döngüsü
		
		int i =1;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		//do-while
		
		int j = 1;
		do {
			System.out.println(j);
			j++;
		}
		while (j<10);
}
}